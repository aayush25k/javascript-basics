// boolean and comparison operator
//boolean :- true/false
let number = 5;
let num= 6;
console.log(number>=num); //output= false;
console.log(number<=num); // output = true;

// == vs ===
// == only check values not datatype;
// === checks both value and datatype;

let num1= 5; //number datatype;
let num2= '5' // string datatype;
console.log(num1==num2); // output = True;
console.log(num1===num2); //output = false;

// != vs !==
// != checks only value and give boolean output;
// !== checks both value and datatype and give boolean output;
console.log(num1!=num2); //output= false;
console.log(num1!==num2); //output = true



